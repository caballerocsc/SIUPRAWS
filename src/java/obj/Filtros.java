public class Filtros {
    private String aliasTipo;
    private String nombreTipo;
    private String textoTipo;
    private String aliasSubTipo;
    private String nombreSubTipo;
    private String valorFiltro;
    private String tipoElemtoPadre;
    private String tipoElemento;

    public Filtros() {
    }

    public String getAliasTipo() {
        return aliasTipo;
    }

    public void setAliasTipo(String aliasTipo) {
        this.aliasTipo = aliasTipo;
    }

    public String getNombreTipo() {
        return nombreTipo;
    }

    public void setNombreTipo(String nombreTipo) {
        this.nombreTipo = nombreTipo;
    }

    public String getTextoTipo() {
        return textoTipo;
    }

    public void setTextoTipo(String textoTipo) {
        this.textoTipo = textoTipo;
    }

    public String getAliasSubTipo() {
        return aliasSubTipo;
    }

    public void setAliasSubTipo(String aliasSubTipo) {
        this.aliasSubTipo = aliasSubTipo;
    }

    public String getNombreSubTipo() {
        return nombreSubTipo;
    }

    public void setNombreSubTipo(String nombreSubTipo) {
        this.nombreSubTipo = nombreSubTipo;
    }

    public String getValorFiltro() {
        return valorFiltro;
    }

    public void setValorFiltro(String valorFiltro) {
        this.valorFiltro = valorFiltro;
    }

    public String getTipoElemtoPadre() {
        return tipoElemtoPadre;
    }

    public void setTipoElemtoPadre(String tipoElemtoPadre) {
        this.tipoElemtoPadre = tipoElemtoPadre;
    }

    public String getTipoElemento() {
        return tipoElemento;
    }

    public void setTipoElemento(String tipoElemento) {
        this.tipoElemento = tipoElemento;
    }
    
}
