package obj;

/**
 *
 * @author Usuario
 */
public class Actores {

    private String codDane;
    private String depto;
    private int numOrg;
    private String categoria;
    private String org;
    private String localizacion;
    private String munpio;
    
    public Actores() {
    }

    public String getCodDane() {
        return codDane;
    }

    public void setCodDane(String codDane) {
        this.codDane = codDane;
    }

    public String getDepto() {
        return depto;
    }

    public void setDepto(String depto) {
        this.depto = depto;
    }

    public int getNumOrg() {
        return numOrg;
    }

    public void setNumOrg(int numOrg) {
        this.numOrg = numOrg;
    }

    public String getCategoria() {
        return categoria;
    }

    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    public String getOrg() {
        return org;
    }

    public void setOrg(String org) {
        this.org = org;
    }

    public String getLocalizacion() {
        return localizacion;
    }

    public void setLocalizacion(String localizacion) {
        this.localizacion = localizacion;
    }

    public String getMunpio() {
        return munpio;
    }

    public void setMunpio(String munpio) {
        this.munpio = munpio;
    }
    
    
}
